#include "libft_x.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*tmp;
	void	*result;

	new_lst = NULL;
	while (lst)
	{
		result = f(lst->content);
		if (result == NULL)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		tmp = ft_lstnew(result);
		if (tmp == NULL)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, tmp);
		lst = lst->next;
	}
	return (new_lst);
}
