int	print_chr(char c)
{
	ft_putchar_fd(c, 1);
	return (1);
}

/*	************************************************************************* */

int	b_print_chr(char c, int width)
{
	int	len;

	len = put_width_chr(width, 1, ' ');
	ft_putchar_fd(c, 1);
	return (len + 1);
}
