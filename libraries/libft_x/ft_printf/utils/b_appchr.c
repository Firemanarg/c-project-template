/*	************************************************************************* */
/*	***********************   D E S C R I P T I O N   *********************** */
/*	************************************************************************* */
/*	*                                                                       * */
/*	*  This function inserts a character at the beginning of a string and   * */
/*	*  return the new generated string.                                     * */
/*	*  In case of errors or null string, the returned value will be NULL.   * */
/*	*                                                                       * */
/*	************************************************************************* */

#include "libft_x.h"

/*	************************************************************************* */
/*	*******************   I M P L E M E N T A T I O N S   ******************* */
/*	************************************************************************* */

char	*b_appchr(char *str, char chr)
{
	size_t	len;
	char	*new_str;

	len = 0;
	if (str)
		len = ft_strlen(str);
	new_str = ft_calloc(len + 2, sizeof(char));
	if (!new_str)
		return (NULL);
	new_str[0] = chr;
	ft_memmove(new_str + 1, str, len);
	new_str[len + 1] = '\0';
	return (new_str);
}

