#include "libft_x.h"

/*	************************************************************************* */
/*	*******************   I M P L E M E N T A T I O N S   ******************* */
/*	************************************************************************* */

int	print_str(char *str)
{
	if (!str)
		return (print_str("(null)"));
	ft_putstr_fd(str, 1);
	return (ft_strlen(str));
}

/*	************************************************************************* */

int	b_print_str(char *str, int width)
{
	int	len;
	int	i;
	int	to_clear;

	to_clear = 0;
	if (!str)
	{
		to_clear = 1;
		str = ft_strdup("(null)");
	}
	len = ft_strlen(str);
	i = 0;
	while (i < width - len)
	{
		ft_putchar_fd(' ', 1);
		i += 1;
	}
	ft_putstr_fd(str, 1);
	if (to_clear)
	{
		free(str);
		str = NULL;
	}
	return (len + i);
}

